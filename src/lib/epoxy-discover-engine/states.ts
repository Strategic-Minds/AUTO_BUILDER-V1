export type StateCode = keyof typeof STATE_NAMES;

export const STATE_CODES = Object.keys(STATE_NAMES) as StateCode[];

export function isStateCode(value: string): value is StateCode {
  return Object.prototype.hasOwnProperty.call(STATE_NAMES, value);
}

export function normalizeStateCode(value?: string | null): StateCode {
  const candidate = value?.trim().toUpperCase();
  if (candidate && isStateCode(candidate)) {
    return candidate;
  }

  return DEFAULT_DRY_RUN_STATE;
}
